package main

import (
	"fmt"
	"sync"
	"time"
)

type Fetcher interface {
	// Fetch returns the body of URL and
	// a slice of URLs found on that page.
	Fetch(url string) (body string, urls []string, err error)
}

type fetchState struct {
	// tracks which urls we have already fetch (with mutext to make concurrent
	// access safe
	mu      sync.Mutex
	fetched map[string]bool
}

// Crawl uses fetcher to recursively crawl
// pages starting with url, to a maximum of depth.
func Crawl(url string, depth int, fetcher Fetcher) {
	// re-entrant map for caching fetched urls
	state := fetchState{fetched: make(map[string]bool)}

	crawl_help(url, depth, fetcher, state)
}

func crawl_help(url string, depth int, fetcher Fetcher, state fetchState) {
	if depth <= 0 {
		return
	}

	state.mu.Lock()
	_, done := state.fetched[url] // optional return value indicates whether key is present in the map
	//done, present := state.fetched[url] // can check for key presence like this
	state.fetched[url] = true
	state.mu.Unlock()

	if done {
		return
	}

	body, urls, err := fetcher.Fetch(url)
	if err != nil {
		fmt.Println(err)
		return
	}

	fmt.Printf("found: %s %q\n", url, body)

	for _, u := range urls {
		go crawl_help(u, depth-1, fetcher, state)
	}
	time.Sleep(50 * time.Millisecond)
	return

}

func main() {
	Crawl("https://golang.org/", 4, fetcher)
}

// fakeFetcher is Fetcher that returns canned results.
type fakeFetcher map[string]*fakeResult

type fakeResult struct {
	body string
	urls []string
}

func (f fakeFetcher) Fetch(url string) (string, []string, error) {
	if res, ok := f[url]; ok {
		return res.body, res.urls, nil
	}
	return "", nil, fmt.Errorf("not found: %s", url)
}

